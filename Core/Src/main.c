/**
  * @brief  Rx FIFO 0 callback.
  * @param  hfdcan: pointer to an FDCAN_HandleTypeDef structure that contains
  *         the configuration information for the specified FDCAN.
  * @param  RxFifo0ITs: indicates which Rx FIFO 0 interrupts are signalled.
  *         This parameter can be any combination of @arg FDCAN_Rx_Fifo0_Interrupts.
  * @retval None
  */
void HAL_FDCAN_RxFifo0Callback(FDCAN_HandleTypeDef *hfdcan, uint32_t RxFifo0ITs)
{
    if((RxFifo0ITs & FDCAN_IT_RX_FIFO0_NEW_MESSAGE) != RESET)
    {
        /* Retrieve Rx messages from RX FIFO0 */
        if (HAL_FDCAN_GetRxMessage(hfdcan, FDCAN_RX_FIFO0, &RxHeader, RxData) == HAL_OK) {
            // Логируем принятый фрейм
            snprintf(trans_str, sizeof(trans_str), "%08lu CanRx: %lX [%ld] ", HAL_GetTick(), RxHeader.Identifier, RxHeader.DataLength);
            for (uint32_t i = 0; i < RxHeader.DataLength; i++) {
                char buffer[4];  // Временный буфер для каждого значения DLC
                snprintf(buffer, sizeof(buffer), "%02X", RxData[i]);  // Преобразуем значение в строку
                strcat(trans_str, buffer);  // Добавляем значение к основной строке
                if (i < 7) {  // Добавляем запятую между значениями, кроме последнего
                    strcat(trans_str, " ");
                }
            }
            strcat(trans_str, "\n");
            HAL_UART_Transmit(&huart2, (uint8_t *) trans_str, strlen(trans_str), 100);

            // Обработка сообщения в зависимости от ID
            if (RxHeader.Identifier == ENGINE_DATA_ID ||  RxHeader.Identifier == GEARBOX_DATA_ID)
                obd2_Handler(RxData);  // Вызываем обработчик данных OBD-II
            } else {
                // Неизвестное сообщение, логируем
                sprintf(trans_str, "Unknown ID: 0x%lX\n", RxHeader.Identifier);
                HAL_UART_Transmit(&huart2, (uint8_t*)trans_str, strlen(trans_str), HAL_MAX_DELAY);
            }
        } else {
            // Логируем ошибку приёма
            sprintf(trans_str, "CAN RX ERROR\n");
            HAL_UART_Transmit(&huart2, (uint8_t*)trans_str, strlen(trans_str), HAL_MAX_DELAY);
            HAL_FDCAN_ErrorCallback(hfdcan);
        }
    }


